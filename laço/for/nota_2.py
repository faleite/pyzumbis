""" REPETIÇÃO COM O 'FOR'
- for é um while mais podereso
- contadores incremento constante
- acumuladores variável
"""

# Para o contador (k) na lista de 1 a 3 (range(1, 4))
# Print k, imprime na tela os números de 1 a 3
# range(1, 4) onde o primeiro parâmetro é o primeiro indice da sequência de números
# e o segundo parâmetro é o número de indices até o número anterior
for k in range(1, 4): print(k)
